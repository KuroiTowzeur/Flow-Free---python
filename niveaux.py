def convert(path='niveaux.txt'):
    with open(path, 'r') as file:
        l = file.readlines()

        niveaux = []
        n = []
        for c in l:
            if c == '\n':
                niveaux.append(n)
                n = []
            else:
                n.append(c)

        niveaux = [[ligne.replace('\n', '') for ligne in niveau] for niveau in niveaux]

        dic_niveaux = {i: {} for i in range(5, 10)}

        for niveau in niveaux:
            numero_niveau, taille_grille, nombre_flux = [int(n) for n in niveau[0].split('-')]

            def arrange(ligne):
                case1 = tuple(int(n) for n in ligne[5:9].replace(' ', '').split(','))
                case2 = tuple(int(n) for n in ligne[13:17].replace(' ', '').split(','))
                return [case1, case2]

            extremités = {ligne[:2]: arrange(ligne) for ligne in niveau[1:]}

            dic_niveaux[taille_grille][numero_niveau] = extremités
    return dic_niveaux


